public class LoopsDemo {

    public static void main(String args[]){

        int a = 0;

        while (a < 100)  {
            System.out.println(a);
            a++;
        }





        for (int b = 0; b < 100; b++) {
            System.out.println("hello from forloop aka for-loop");


        }


        System.out.println("Loop Phinished.");

        a=0;
        do{
            System.out.println("Hello, World.");
            a++;
        }while (a < 10);

        }
}
